public class VovelOrConsonant {

	public static void main(String[] args) {
		char ch = 'z';
		switch (ch) {
		case 'a':
			System.out.println("Character is Vovel");
			break;
		case 'e':
			System.out.println("Character is Vovel");
			break;
		case 'i':
			System.out.println("Character is Vovel");
			break;
		case 'o':
			System.out.println("Character is Vovel");
			break;
		case 'u':
			System.out.println("Character is Vovel");
			break;
		default:
			System.out.println("Character is Consonant");
			break;

		}

	}

}
